package purchases.approve.purchase;

import purchases.approve.common.Type;

public class Purchase {
    private int id;
    private Type type;
    private double price;


    public Purchase(int id, Type type, double price) {
        this.id = id;
        this.type = type;
        this.price = price;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Type getType() {
        return type;
    }

    public void setType(Type type) {
        this.type = type;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
}
